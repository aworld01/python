'''
The mechanism of deriving a new class from an old one (existing class) such that the new class inherit all the members (variables and methods) of old class is called inheritance or derivation.
'''
'''
The old class is referred to as the Super_class and the new one is called the Sub_class.

Parent_class - Base_class - Super_class
Child_class - Derived_class - Sub_class

All classes in python are built from a single super class called 'object' so whenever we create a class in python, object become super class for them internally.

The main advantage of inheritance is code reusability
'''
